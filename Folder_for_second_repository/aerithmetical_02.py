# Python program to do arithmetical operations
a = input("Enter the integer number:")
b = input("Enter the integer number:")

add = int(a) + int(b)
print(add)

minus = int(a) - int(b)
print(minus)

mul = int(a) * int(b)
print(mul)

div = int(a) / int(b)
print(div)

double = int(a) // int(b)
print(double)

power = int(a) ^ int(b)
print(power)

mo = int(a) % int(b)
print(mo)



